//Hands-on exercise #2
//Using the following operators, write expressions and assign their values to variables:
//==
//<=
//>=
//!=
//<
//>

package main

import "fmt"

func main() {
	a := 10
	b := 100
	d := a == b
	e := a <= b
	f := a >= b
	g := a != b
	h := a < b
	i := a > b

	fmt.Println(a, b, d, e, f, g, h, i)

}
